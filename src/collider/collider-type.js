class ColliderType {
    
    /**
     * A basic type collider, meaning walls, ground and bodies.
     *
     * @name ColliderType#BASIC
     * @type {ColliderType.BASIC}
     * @since 0.0.0
     */
    static BASIC = 0;

    /**
     * A collider wich is used to inflict damage or negative stats.
     *
     * @name ColliderType#BASIC
     * @type {ColliderType.BASIC}
     * @since 0.0.0
     */
    static HARM = 1;

    /**
     * A collider used to inflict heal or good stats.
     *
     * @name ColliderType#BASIC
     * @type {ColliderType.BASIC}
     * @since 0.0.0
     */
    static SUPPORT = 2;
}